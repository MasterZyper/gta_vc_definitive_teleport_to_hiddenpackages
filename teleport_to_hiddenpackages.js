//Teleport Player
function teleportToHiddenPackage(index) {
    if (index < 0 || index >= hiddenPackagePositions.length) return;

    let player = new Player(0);
    let coords = hiddenPackagePositions[index];
    player.setCoordinates(coords.x, coords.y, coords.z);
    Text.Print('Teleport zu Hidden Package #${index + 1}', 2000);
}

while (true) {
    wait(100);

    if (Pad.IsKeyPressed(116)) { // F5 - Jump to the point before
        teleportIndex = (teleportIndex + 1) % hiddenPackagePositions.length;
        teleportToHiddenPackage(teleportIndex);
        wait(500); 
    }

    if (Pad.IsKeyPressed(117)) { // F6 - Jump to the next point
        teleportIndex = (teleportIndex - 1 + hiddenPackagePositions.length) % hiddenPackagePositions.length;
        teleportToHiddenPackage(teleportIndex);
        wait(500);
    }
}
